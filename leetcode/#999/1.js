/**
 * @param {character[][]} board
 * @return {number}
 */
var numRookCaptures = function(board) {
    let m = n =  -1,
        ans = 0,
        i = j = 0;
    for (i = 0; i < board.length; i++) {
        for (j = 0; j < board[i].length; j++) {
            if (board[i][j] === "R") {
                break;
            }
        }
        if (board[i][j] === "R") {
            m = i;
            n = j;
            break;
        }
    }
    if(m === -1 || n === -1) return 0;
    let E =  W = n, S = N = m;
    while (N > 0) {
        N -= 1;
        if(board[N][n] === "p") {
            ans += 1;
            break;
        } 
        if (board[N][n] === "B") break;
    }
    while (S < board.length - 1) {
        S += 1;
        if(board[S][n] === "p") {
            ans += 1;
            break;
        } 
        if (board[S][n] === "B") break;
    }
    while (E > 0) {
        E -= 1;
        if(board[m][E] === "p") {
            ans += 1;
            break;
        } 
        if (board[m][E] === "B") break;
    }
    while (W < board[m].length - 1) {
        W += 1;
        if(board[m][W] === "p") {
            ans += 1;
            break;
        } 
        if (board[m][W] === "B") break;
    }
    return ans;
};

