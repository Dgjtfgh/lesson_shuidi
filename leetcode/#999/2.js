/**
 * @param {character[][]} board
 * @return {number}
 */
var numRookCaptures = function(board) {
    let col = [],
        row = [],
        ans = 0,
        row_sub,
        col_sub;
    board.forEach(item => {
        if(item.indexOf("R") > -1) {
            row_sub = item.indexOf("R");
            row = item.filter(item => item !== '.');
        }
    })
    board.forEach(item => {
        if(item[row_sub] !== '.') col.push(item[row_sub]);
    })
    col_sub = col.indexOf("R");
    row_sub = row.indexOf("R");
    if(col[col_sub-1] && col[col_sub-1] === 'p') ans += 1;
    if(col[col_sub+1] && col[col_sub+1] === 'p') ans += 1;
    if(row[row_sub-1] && row[row_sub-1] === 'p') ans += 1;
    if(row[row_sub+1] && row[row_sub+1] === 'p') ans += 1;
    return ans;
};

